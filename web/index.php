С помощью дробеструйной обработки мы можем обнаружить ранее невидимые дефекты, которые могут вызвать трещины в недавно проложенном промышленном полу. 
<p>
Мы используем оборудование последнего поколения Blastrac для выполнения работ с использованием метода дробеструйной обработки, благодаря чему технология обслуживания является высокоэффективной и экологически чистой. 
</p>

