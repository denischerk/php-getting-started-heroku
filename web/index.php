			<div class="col-lg-5 col-sm-5"><div><p>Мы имеем большой опыт в области подготовки субстрата и ремонта пола. Именно поэтому мы предлагаем клиентам широкий ассортимент, который включает в себя помимо струйного и фрезерного бетона, также профессиональное шлифование бетона. Шлифовальные полы - чрезвычайно эффективный метод механической обработки. Его чаще всего используют на завершающей стадии работ. Использование шлифовальных станков направлено на снижение шероховатости поверхности и выравнивание неровностей. </p>
