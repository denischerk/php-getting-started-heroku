<h4>ШЛИФОВКА</h4>
<div class="container-fluid">
			<div class="col-lg-1 col-sm-1"></div>
			<div class="col-lg-5 col-sm-5"><div><p>Фрезерный бетон - еще один метод конкретной обработки, который мы предлагаем в нашем предложении. Этот метод используется в случае сильных повреждений в плоскости бетона. Фрезерные полы предназначены для очистки, профилирования и разрушения существующей поверхности. В результате мы получаем чистое, рифленое основание, которое отличается лучшей адгезией последующих слоев. Этот метод подходит практически для любого типа напольных покрытий, как из бетона, так и пола из смолы.</p> 
<p>
Фрезерование также используется при резке бетонных поверхностей в коровниках, что придает полу грубую текстуру и, как следствие, снижает риск скольжения животных. 
</p>
<p>
Мы также предлагаем нашим клиентам профессиональные услуги, включая шлифование бетона.
</p>
<div class="content_prim">ПРИМЕНЕНИЕ:</div>
<ul>
	<li>Удаление разрушенных слоев бетонных, полимерных и асфальтовых полов</li>
	<li>Профилирование гладких поверхностей</li>
	<li>Выравнивание и очистка поверхности</li>
	</ul>
</div></div>
			

